import math  # Esto es un modulo / libreria.

print(
    round(1.3)
)  # Me redondea el numero, si esta en la mitad +1 entonces me devuelve el techo, si esta en la mitad -1 me devuelve el piso
print(round(1.7))
print(round(1.5))
print(
    abs(-77)
)  # abs me devuelve le valor absoluto, es decir, saca el valor negativo, solo me devuelve el positivo.
print(abs(55))

# Un modulo es un archivo que viene escrito en python.

print(math.ceil(1.1))  # Me devuelve el techo
print(math.floor(1.99999999999))  # Me devuelve el piso
print(
    math.isnan()
)  # Me devuelve si el valor que nosotros le pasamos corresponde a que NO ES un numero.
# Isnan solo sirve si le paso numeros reales.
print(math.pow(10, 3))  # Elevo 10 a la 3
print(math.sqrt(9))  # Saca la raiz cuadrada del numero indicado

